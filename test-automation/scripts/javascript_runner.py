def parse_js_functions(file_path: str) -> Dict:
    """Parse JavaScript/TypeScript file to extract function signatures."""
    path = Path(file_path)
    if not path.exists():
        return {"functions": [], "classes": []}
    
    content = path.read_text()
    
    functions = []
    classes = []
    
    # Match function declarations: function name(args) or const name = (args) =>
    func_patterns = [
        r'(?:export\s+)?(?:async\s+)?function\s+(\w+)\s*\(([^)]*)\)',
        r'(?:export\s+)?const\s+(\w+)\s*=\s*(?:async\s+)?\(([^)]*)\)\s*=>',
        r'(?:export\s+)?(?:const|let|var)\s+(\w+)\s*=\s*(?:async\s+)?function\s*\(([^)]*)\)',
    ]
    
    for pattern in func_patterns:
        for match in re.finditer(pattern, content):
            func_name = match.group(1)
            args = match.group(2).strip()
            
            # Skip private/internal
            if func_name.startswith("_"):
                continue
            
            functions.append({
                "name": func_name,
                "args": [a.strip() for a in args.split(",") if a.strip()] if args else [],
                "is_async": "async" in match.group(0)
            })
    
    # Match class declarations
    class_pattern = r'(?:export\s+)?class\s+(\w+)(?:\s+extends\s+\w+)?\s*\{'
    for match in re.finditer(class_pattern, content):
        class_name = match.group(1)
        
        # Find methods in class
        class_start = match.end()
        # Simple bracket matching to find class body
        brace_count = 1
        class_end = class_start
        for i, char in enumerate(content[class_start:]):
            if char == "{":
                brace_count += 1
            elif char == "}":
                brace_count -= 1
                if brace_count == 0:
                    class_end = class_start + i
                    break
        
        class_body = content[class_start:class_end]
        methods = []
        
        method_pattern = r'(?:async\s+)?(\w+)\s*\(([^)]*)\)\s*\{'
        for m in re.finditer(method_pattern, class_body):
            method_name = m.group(1)
            if not method_name.startswith("_") and method_name not in ["constructor", "get", "set"]:
                methods.append({
                    "name": method_name,
                    "args": [a.strip() for a in m.group(2).split(",") if a.strip()] if m.group(2) else []
                })
        
        classes.append({
            "name": class_name,
            "methods": methods
        })
    
    return {"functions": functions, "classes": classes}


def generate_jest_tests(file_path: str, output_path: Optional[str] = None) -> Dict:
    """Generate Jest test scaffolding for a JS/TS file."""
    path = Path(file_path)
    parsed = parse_js_functions(file_path)
    
    module_name = path.stem
    ext = path.suffix  # .js, .ts, .jsx, .tsx
    test_ext = ".test" + ext
    
    if output_path:
        output = Path(output_path)
        if output.is_dir():
            test_file_path = output / (module_name + test_ext)
        else:
            test_file_path = output
    else:
        test_file_path = path.parent / (module_name + test_ext)
    
    # Determine import style
    if ext in [".ts", ".tsx"]:
        import_line = f'import * as {module_name} from "./{module_name}";'
    else:
        import_line = f'const {module_name} = require("./{module_name}");'
    
    lines = [
        f'/**',
        f' * Tests for {path.name}',
        f' * Auto-generated by test-automation skill',
        f' */',
        f'',
        import_line,
        f'',
        f'describe("{module_name}", () => {{',
        f'  ',
    ]
    
    # Generate tests for functions
    for func in parsed.get("functions", []):
        func_name = func["name"]
        is_async = func.get("is_async", False)
        args = func.get("args", [])
        
        lines.extend([
            f'  describe("{func_name}", () => {{',
            f'    it("should work correctly", async () => {{',
            f'      // Arrange',
        ])
        
        if args:
            lines.append(f'      // TODO: Set up test inputs for: {", ".join(args)}')
        
        lines.extend([
            f'      ',
            f'      // Act',
        ])
        
        if is_async:
            lines.append(f'      const result = await {module_name}.{func_name}();')
        else:
            lines.append(f'      const result = {module_name}.{func_name}();')
        
        lines.extend([
            f'      ',
            f'      // Assert',
            f'      // TODO: Add appropriate assertions',
            f'      expect(result).toBeDefined();',
            f'    }});',
            f'  }});',
            f'  ',
        ])
    
    # Generate tests for classes
    for cls in parsed.get("classes", []):
        class_name = cls["name"]
        lines.extend([
            f'  describe("{class_name}", () => {{',
            f'    let instance;',
            f'    ',
            f'    beforeEach(() => {{',
            f'      instance = new {module_name}.{class_name}();',
            f'    }});',
            f'    ',
        ])
        
        for method in cls.get("methods", []):
            method_name = method["name"]
            lines.extend([
                f'    describe("{method_name}", () => {{',
                f'      it("should work correctly", () => {{',
                f'        // TODO: Implement test',
                f'        expect(instance.{method_name}).toBeDefined();',
                f'      }});',
                f'    }});',
                f'    ',
            ])
        
        lines.extend([
            f'  }});',
            f'  ',
        ])
    
    lines.extend([
        f'}});',
        f'',
    ])
    
    test_content = "\n".join(lines)
    test_file_path.write_text(test_content)
    
    return {
        "success": True,
        "test_file": str(test_file_path),
        "functions_found": len(parsed.get("functions", [])),
        "classes_found": len(parsed.get("classes", [])),
        "output": f"Generated test file: {test_file_path}\n{test_content}"
    }


def run_jest_tests(test_path: str, framework: str = "jest") -> Dict:
    """Run Jest tests and return results."""
    path = Path(test_path)
    
    # Find project root
    project_root = path
    while project_root.parent != project_root:
        if (project_root / "package.json").exists():
            break
        project_root = project_root.parent
    
    # Determine test runner command
    if framework == "vitest":
        cmd = ["npx", "vitest", "run", str(path), "--reporter=verbose"]
    elif framework == "mocha":
        cmd = ["npx", "mocha", str(path)]
    else:
        # Default to Jest
        cmd = ["npx", "jest", str(path), "--verbose"]
    
    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=300,
            cwd=project_root
        )
        
        output = result.stdout + result.stderr
        
        # Parse Jest output
        passed = len(re.findall(r'✓|✔|PASS', output))
        failed = len(re.findall(r'✕|✖|FAIL', output))
        
        # Extract test suites info
        suites_match = re.search(r'Test Suites:\s+(\d+)\s+passed.*?,(\d+)\s+failed', output)
        tests_match = re.search(r'Tests:\s+(\d+)\s+passed.*?,(\d+)\s+failed', output)
        
        return {
            "success": result.returncode == 0,
            "exit_code": result.returncode,
            "passed": passed,
            "failed": failed,
            "output": output
        }
    except subprocess.TimeoutExpired:
        return {
            "success": False,
            "error": "Test execution timed out (5 minutes)",
            "output": ""
        }
    except Exception as e:
        return {
            "success": False,
            "error": str(e),
            "output": ""
        }


