def run_mutation_test(file_path: str) -> Dict:
    """Run mutation testing using Stryker (if available)."""
    path = Path(file_path)
    
    project_root = path
    while project_root.parent != project_root:
        if (project_root / "package.json").exists():
            break
        project_root = project_root.parent
    
    # Check if Stryker is configured
    stryker_config = project_root / "stryker.config.json"
    has_stryker = stryker_config.exists()
    
    if not has_stryker:
        # Create basic Stryker config
        stryker_config.write_text(json.dumps({
            "$schema": "https://unpkg.com/@stryker-mutator/core/schema/stryker-schema.json",
            "packageManager": "npm",
            "reporters": ["html", "clear-text", "progress"],
            "testRunner": "jest",
            "coverageAnalysis": "perTest"
        }, indent=2))
    
    try:
        # Run Stryker
        result = subprocess.run(
            ["npx", "stryker", "run"],
            capture_output=True,
            text=True,
            timeout=600,
            cwd=project_root
        )
        
        output = result.stdout + result.stderr
        
        # Parse mutation score
        score_match = re.search(r'([\d.]+)%', output)
        mutation_score = float(score_match.group(1)) if score_match else 0
        
        return {
            "success": result.returncode == 0,
            "mutation_score": mutation_score,
            "output": output
        }
        
    except Exception as e:
        return {
            "success": False,
            "error": str(e),
            "output": ""
        }


def run_benchmark(file_path: str) -> Dict:
    """Run performance benchmarks using benchmark.js."""
    path = Path(file_path)
    parsed = parse_js_functions(file_path)
    
    # Create benchmark script
    benchmark_code = f'''
const Benchmark = require('benchmark');
const module = require('{path.stem}');

const suite = new Benchmark.Suite();

'''
    
    for func in parsed.get("functions", []):
        func_name = func["name"]
        if not func_name.startswith("_"):
            benchmark_code += f'''
suite.add('{func_name}', () => {{
  module.{func_name}();
}});
'''
    
    benchmark_code += '''
suite
  .on('cycle', (event) => {
    console.log(String(event.target));
  })
  .on('complete', function() {
    const results = this.map(bench => ({
      name: bench.name,
      hz: bench.hz,
      stats: bench.stats
    }));
    console.log('---RESULTS---');
    console.log(JSON.stringify(results, null, 2));
  })
  .run({ async: true });
'''
    
    try:
        result = subprocess.run(
            ["node", "-e", benchmark_code],
            capture_output=True,
            text=True,
            timeout=60,
            cwd=path.parent
        )
        
        output = result.stdout
        
        # Parse results
        results_match = re.search(r'---RESULTS---\n(.+)', output, re.DOTALL)
        benchmarks = []
        if results_match:
            try:
                benchmarks = json.loads(results_match.group(1))
            except:
                pass
        
        return {
            "success": result.returncode == 0,
            "benchmarks": benchmarks,
            "output": output
        }
        
    except Exception as e:
        return {
            "success": False,
            "error": str(e),
            "output": ""
        }


def main():
    parser = argparse.ArgumentParser(description="JavaScript test runner")
    parser.add_argument("operation", choices=["generate", "run", "coverage", "mutation", "benchmark"])
    parser.add_argument("file_path")
    parser.add_argument("--framework", default="jest")
    parser.add_argument("--output")
    
    args = parser.parse_args()
    
    result = {}
    
    if args.operation == "generate":
        result = generate_jest_tests(args.file_path, args.output)
    elif args.operation == "run":
        result = run_jest_tests(args.file_path, args.framework)
    elif args.operation == "coverage":
        result = run_coverage(args.file_path, args.framework)
    elif args.operation == "mutation":
        result = run_mutation_test(args.file_path)
    elif args.operation == "benchmark":
        result = run_benchmark(args.file_path)
    
    print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()
